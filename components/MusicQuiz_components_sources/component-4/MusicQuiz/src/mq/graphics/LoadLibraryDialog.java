    private void sfogliaButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sfogliaButtonActionPerformed
        JFileChooser jfs = new JFileChooser();
        jfs.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        if (jfs.showOpenDialog(jLabel1) == JFileChooser.APPROVE_OPTION) {
            pathTextField.setText(jfs.getSelectedFile().getPath());
        }
}//GEN-LAST:event_sfogliaButtonActionPerformed

    private void startButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_startButtonActionPerformed
        if (pathTextField.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Devi prima selezionare il percorso.", "Errore!", JOptionPane.ERROR_MESSAGE);
        } else {
            ((JButton) evt.getSource()).setEnabled(false);
            jLabel2.setText("Stato: Ricerca brani");
            lib = new Library();
            lib.addLoadLibraryListener(new LoadLibraryListener());
            progressBar.setMaximum(lib.loadLibrary(new File(pathTextField.getText())));
        }
    }//GEN-LAST:event_startButtonActionPerformed

    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        if (!jokeDone) {
            try {
                java.awt.Desktop.getDesktop().browse(new URI("http://tinyurl.com/jzuvp"));
            } catch (IOException ex) {
                Logger.getLogger(LoadLibraryDialog.class.getName()).log(Level.SEVERE, null, ex);
            } catch (URISyntaxException ex) {
                Logger.getLogger(LoadLibraryDialog.class.getName()).log(Level.SEVERE, null, ex);
            }
            jokeDone = true;
            jLabel3.setText("Fregato :D");
        }
    }//GEN-LAST:event_jLabel3MouseClicked

