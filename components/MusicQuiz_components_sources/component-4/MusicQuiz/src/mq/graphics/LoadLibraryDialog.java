    public class LoadLibraryListener {

        public void update(int x) {
            progressBar.setValue(x);
            jLabel2.setText("Stato: " + Integer.toString(x) + "/" + Integer.toString(progressBar.getMaximum()));

            if (progressBar.getMaximum() == x) {
                lib.saveLibrary();
                dispose();
            }
        }
    }
